#include <unistd.h>

void		ft_putnbr(int n)
{
	char digit;

	if (n < 10 && n > -10)
	{
		if (n < 0)
			write(1, "-", 1);
	}
	else
		ft_putnbr(n / 10);
	digit = ((n < 0) ? -1 : 1) * (n % 10) + '0';
	write(1, &digit, 1);
}
